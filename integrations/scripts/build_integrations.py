TOOL_DIRS = ("cursor", "codex", "intellij")


def build_all() -> dict[str, str]:
    out: dict[str, str] = {}
    out.update(_cursor())
    out.update(_codex())
    out.update(_intellij())
    return out


def main() -> int:
    parser = argparse.ArgumentParser(
        description="Generate Cursor / IntelliJ / Codex integrations from the Claude skills."
    )
    parser.add_argument(
        "--check",
        action="store_true",
        help="Exit 1 if any generated file is missing or stale (do not write).",
    )
    args = parser.parse_args()

    files = build_all()

    if args.check:
        stale: list[str] = []
        for rel, content in files.items():
            path = INTEGRATIONS_DIR / rel
            existing = path.read_text(encoding="utf-8") if path.is_file() else None
            if existing != content:
                stale.append(rel)
        # detect orphan files under the generated tool dirs
        expected = {(INTEGRATIONS_DIR / rel).resolve() for rel in files}
        for tool in TOOL_DIRS:
            base = INTEGRATIONS_DIR / tool
            if base.is_dir():
                for path in base.rglob("*"):
                    if path.is_file() and path.resolve() not in expected:
                        stale.append(str(path.relative_to(INTEGRATIONS_DIR)) + " (orphan)")
        if stale:
            print("Integrations are stale. Run `make integrations` and commit:", file=sys.stderr)
            for rel in sorted(stale):
                print(f"  - {rel}", file=sys.stderr)
            return 1
        print(f"Integrations up to date ({len(files)} files).")
        return 0

    # write mode: own the three tool dirs entirely
    import shutil

    for tool in TOOL_DIRS:
        base = INTEGRATIONS_DIR / tool
        if base.exists():
            shutil.rmtree(base)
    for rel, content in files.items():
        path = INTEGRATIONS_DIR / rel
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content, encoding="utf-8", newline="\n")
    print(f"Wrote {len(files)} files across {', '.join(TOOL_DIRS)}.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
